// String Methods

// --------------------------------------------------------------------------------------------

// trim() - Trims whitespaces from both ends of the string and returns a new one.

let msg = "  Hello  ";

console.log(msg.trim());

// --------------------------------------------------------------------------------------------

// toUpperCase(), toLowerCase()

let str = "Random String";

console.log(str.toUpperCase());
console.log(str.toLowerCase());

// --------------------------------------------------------------------------------------------

// indexOf("subString") - Returns the "first index of occurrence" of some value in the string. Or gives -1 if not found

str = "ILoveCoding";
console.log(str.indexOf("Coding"));
console.log(str.indexOf("z"));

